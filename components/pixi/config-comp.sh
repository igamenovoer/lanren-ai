#!/usr/bin/env sh
set -eu

script_dir=$(CDPATH= cd "$(dirname "$0")" && pwd)

# Self-contained helpers (inlined per script).
lr_os() {
  # outputs: linux | macos | unknown
  case "$(uname -s 2>/dev/null || echo unknown)" in
    Linux) echo linux ;;
    Darwin) echo macos ;;
    *) echo unknown ;;
  esac
}

lr_arch() {
  # outputs: amd64 | arm64 | <raw>
  case "$(uname -m 2>/dev/null || echo unknown)" in
    x86_64|amd64) echo amd64 ;;
    aarch64|arm64) echo arm64 ;;
    *) uname -m 2>/dev/null || echo unknown ;;
  esac
}

lr_has_cmd() {
  command -v "$1" >/dev/null 2>&1
}

lr_shell_quote() {
  # Single-quote a string for POSIX shells.
  # Example: abc'def -> 'abc'"'"'def'
  printf "'%s'" "$(printf "%s" "${1-}" | sed "s/'/'\"'\"'/g")"
}

lr_set_proxy_env() {
  proxy="${1-}"
  if [ -n "$proxy" ]; then
    export HTTP_PROXY="$proxy" HTTPS_PROXY="$proxy"
    export http_proxy="$proxy" https_proxy="$proxy"
  fi
}

lr_init_component_log() {
  LR_COMPONENT_NAME="${1-unknown}"
  LR_CAPTURE_LOG_FILE="${2-}"
  LR_DRY_RUN="${3-0}"

  if [ -n "${LRAI_MASTER_OUTPUT_DIR:-}" ]; then
    LR_ROOT="$LRAI_MASTER_OUTPUT_DIR"
  else
    LR_ROOT="$(pwd)/lanren-cache"
  fi

  LR_LOG_DIR="$LR_ROOT/logs/$LR_COMPONENT_NAME"
  LR_PKG_DIR="$LR_ROOT/packages/$LR_COMPONENT_NAME"
  mkdir -p "$LR_LOG_DIR" "$LR_PKG_DIR"

  ts="$(date +%Y%m%d_%H%M%S 2>/dev/null || date)"
  LR_LOG_FILE="$LR_LOG_DIR/$LR_COMPONENT_NAME-$ts.log"
  : >"$LR_LOG_FILE"

  if [ -n "$LR_CAPTURE_LOG_FILE" ]; then
    cap_dir="$(dirname "$LR_CAPTURE_LOG_FILE" 2>/dev/null || echo .)"
    mkdir -p "$cap_dir"
    : >"$LR_CAPTURE_LOG_FILE"
  fi
}

lr_log() {
  msg="$*"
  if [ -n "${LR_CAPTURE_LOG_FILE:-}" ]; then
    printf '%s\n' "$msg" | tee -a "$LR_LOG_FILE" "$LR_CAPTURE_LOG_FILE"
  else
    printf '%s\n' "$msg" | tee -a "$LR_LOG_FILE"
  fi
}

lr_warn() {
  lr_log "WARNING: $*"
}

lr_err() {
  lr_log "ERROR: $*"
}

lr_die() {
  lr_err "$*"
  exit 1
}

lr_run_impl() {
  if [ "${LR_DRY_RUN:-0}" -eq 1 ]; then
    return 0
  fi
  tmp_out="$(mktemp "${TMPDIR:-/tmp}/lanren-ai.XXXXXX" 2>/dev/null || echo "${TMPDIR:-/tmp}/lanren-ai.$$.$(date +%s)")"
  tmp_rc="$tmp_out.rc"

  (
    set +e
    "$@" >"$tmp_out" 2>&1
    echo $? >"$tmp_rc"
  )

  if [ -n "${LR_CAPTURE_LOG_FILE:-}" ]; then
    cat "$tmp_out" | tee -a "$LR_LOG_FILE" "$LR_CAPTURE_LOG_FILE"
  else
    cat "$tmp_out" | tee -a "$LR_LOG_FILE"
  fi

  rc="$(cat "$tmp_rc" 2>/dev/null || echo 1)"
  rm -f "$tmp_out" "$tmp_rc" 2>/dev/null || true
  return "$rc"
}

lr_run() {
  # Runs a command and logs its combined output while preserving the command exit code.
  # Usage: lr_run <cmd> [args...]
  lr_log "+ $*"
  lr_run_impl "$@"
}

lr_run_masked() {
  # Runs a command but logs a caller-provided, redacted description instead of the full argv.
  # Usage: lr_run_masked "<safe description>" <cmd> [args...]
  desc="${1-}"
  shift || true
  lr_log "+ $desc"
  lr_run_impl "$@"
}

lr_download() {
  # Usage: lr_download <url> <dest_path>
  url="$1"
  dest="$2"
  if lr_has_cmd curl; then
    lr_run curl -fsSL "$url" -o "$dest"
    return $?
  fi
  if lr_has_cmd wget; then
    lr_run wget -q "$url" -O "$dest"
    return $?
  fi
  lr_die "Neither curl nor wget is available for downloading: $url"
}

lr_sudo() {
  # Echo a sudo prefix if needed/available.
  if [ "$(id -u 2>/dev/null || echo 1)" -eq 0 ]; then
    echo ""
    return 0
  fi
  if lr_has_cmd sudo; then
    echo "sudo"
    return 0
  fi
  echo ""
}

usage() {
  cat <<'EOF'
Usage: ./config-comp.sh [options]

Configure pixi global mirrors (conda-forge and PyPI).

Options:
  --mirror <cn|official>                     Preset for both conda+PyPI (default: cn)
  --mirror-conda <cn|official|tuna>          Conda mirror override
  --mirror-pypi <cn|official|aliyun|tuna>    PyPI mirror override
  --dry-run                                  Print what would change, without applying
  --capture-log-file PATH                    Also write logs to PATH
  -h, --help                                 Show this help
EOF
}

mirror=""
mirror_conda=""
mirror_pypi=""
dry_run=0
capture_log_file=""

while [ $# -gt 0 ]; do
  case "$1" in
    --mirror) mirror="${2-}"; shift 2 ;;
    --mirror=*) mirror="${1#*=}"; shift ;;
    --mirror-conda) mirror_conda="${2-}"; shift 2 ;;
    --mirror-conda=*) mirror_conda="${1#*=}"; shift ;;
    --mirror-pypi) mirror_pypi="${2-}"; shift 2 ;;
    --mirror-pypi=*) mirror_pypi="${1#*=}"; shift ;;
    --dry-run) dry_run=1; shift ;;
    --capture-log-file) capture_log_file="${2-}"; shift 2 ;;
    --capture-log-file=*) capture_log_file="${1#*=}"; shift ;;
    -h|--help) usage; exit 0 ;;
    *) echo "Unknown argument: $1" >&2; usage; exit 1 ;;
  esac
done

if [ -z "$mirror" ] && [ -z "$mirror_conda" ] && [ -z "$mirror_pypi" ]; then
  mirror="cn"
fi

case "$mirror" in
  ""|cn|official) ;;
  *) echo "Invalid --mirror: $mirror" >&2; usage; exit 1 ;;
esac

case "$mirror_conda" in
  ""|cn|official|tuna) ;;
  *) echo "Invalid --mirror-conda: $mirror_conda" >&2; usage; exit 1 ;;
esac

case "$mirror_pypi" in
  ""|cn|official|aliyun|tuna) ;;
  *) echo "Invalid --mirror-pypi: $mirror_pypi" >&2; usage; exit 1 ;;
esac

component_name=$(basename "$script_dir")
lr_init_component_log "$component_name" "$capture_log_file" "$dry_run"
lr_log "=== Configuring pixi Global Mirrors ==="

if ! lr_has_cmd pixi; then
  lr_die "pixi command not found. Install pixi first."
fi

urls_tuna_conda="https://mirrors.tuna.tsinghua.edu.cn/anaconda/cloud/conda-forge"
urls_tuna_pypi="https://pypi.tuna.tsinghua.edu.cn/simple"
urls_aliyun_pypi="https://mirrors.aliyun.com/pypi/simple/"

effective_conda="$mirror"
effective_pypi="$mirror"

[ -n "$mirror_conda" ] && effective_conda="$mirror_conda"
[ -n "$mirror_pypi" ] && effective_pypi="$mirror_pypi"

# aliases
[ "$effective_conda" = "cn" ] && effective_conda="tuna"
[ "$effective_pypi" = "cn" ] && effective_pypi="tuna"

# Conda config
if [ -n "$effective_conda" ]; then
  if [ "$effective_conda" = "official" ]; then
    lr_log "Unsetting conda-forge mirrors..."
    lr_run pixi config unset mirrors --global || true
  elif [ "$effective_conda" = "tuna" ]; then
    json_val="{ \"https://conda.anaconda.org/conda-forge\": [\"$urls_tuna_conda\"] }"
    lr_log "Setting conda-forge mirror to tuna ($urls_tuna_conda)..."
    lr_run pixi config set mirrors "$json_val" --global
  fi
fi

# PyPI config
pypi_key="pypi-config.index-url"
if [ -n "$effective_pypi" ]; then
  if [ "$effective_pypi" = "official" ]; then
    lr_log "Unsetting PyPI index-url..."
    lr_run pixi config unset "$pypi_key" --global || true
  else
    pypi_url=""
    [ "$effective_pypi" = "tuna" ] && pypi_url="$urls_tuna_pypi"
    [ "$effective_pypi" = "aliyun" ] && pypi_url="$urls_aliyun_pypi"
    if [ -n "$pypi_url" ]; then
      lr_log "Setting PyPI index-url to $effective_pypi ($pypi_url)..."
      lr_run pixi config set "$pypi_key" "$pypi_url" --global
    fi
  fi
fi

lr_log "Configuration complete."
